#include <stdio.h>

int main(void) {
	// x, y, z를 선언하고
	// x는 내 캐릭터가
	// 몇 햏인지 상태를 인지하는

	int x, y, z;

	// 10, 10+20, 30*40을 저장

	x = 10;

	y = 10 + 20;

	z = 30 * 40;

	// x, y, z, x+y 츌력
	printf("x=%d\n", x);

	printf("y=%d\n", y);

	printf("z=%d\n", z);

	printf("x + y =%d\n", x+y);

    return 0;
}